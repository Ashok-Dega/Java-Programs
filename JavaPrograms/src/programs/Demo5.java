package programs;

public class Demo5 {
	
	public static void main(String[] args) {
		int a = 90;
		
		switch (a) {
		case 10:
			System.out.println("10");
			break;
		case 90:
			System.out.println("90");
			break;

		default:
			System.out.println("Not Matched");
			break;
		}
		
		System.out.println("End of Switch");
		
	}

}
